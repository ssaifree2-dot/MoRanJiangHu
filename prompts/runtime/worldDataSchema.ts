- 子地点层级仅用于独立的完整建筑（如”悦来客栈””黑塔机关城”），不用于建筑内的单个房间（如”大厅””卧室”等）。房间属于建筑内部，不单独占地图节点。
- 名称要正式具体，如”洛阳城””华山派””悦来客栈””藏经阁”，禁止使用”未命名建筑””建筑1”等占位名。
