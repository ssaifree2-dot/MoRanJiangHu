export const 构建开局世界演变初始化上下文 = (params: {
    openingBodyText?: string;
    openingPlanText?: string;
    openingCommandTexts?: string[];
    currentGameTime?: string;
}): string => [
    '【开局世界演变初始化上下文】',
    `- 当前游戏内时间：${(params.currentGameTime || '').trim() || '未知时间'}`,
    '- 这是开局生成后的首次后台世界初始化。',
    '',
    '【开局正文】',
    (params.openingBodyText || '').trim() || '暂无',
    '',
    '【开局规划】',
    (params.openingPlanText || '').trim() || '暂无',
    '',
    '【开局已落地命令】',
    ...(Array.isArray(params.openingCommandTexts) && params.openingCommandTexts.length > 0
        ? params.openingCommandTexts.map(c => `- ${c}`)
        : ['- 无']),
].join('\n');
